//Write a program that counts up from 0 to 50 in increments of 1.

#include <iostream>
using namespace std;

int main()
{
    // Variable to store the value to be incremented
    int num = 0;

    // Loop to increment the value of num from 0 to 50
    for (int i = 0; i <= 50; i++)
    {
        // Print the current value of num
        cout << num << " ";

        // Increment num by 1
        num++;
    }


}




